function drawHPBars () {
    if (playerBar == null || bossBar == null) {
        return
    }
    pImg = image.create(50, 6)
    pImg.fillRect(0, 0, 50, 6, 14)
    let pColor = playerHP > 50 ? 7 : (playerHP > 25 ? 5 : 2)
pImg.fillRect(1, 1, Math.max(0, Math.floor(48 * playerHP / 100)), 4, pColor)
    pImg.drawRect(0, 0, 50, 6, 15)
    playerBar.setImage(pImg)
    bImg = image.create(50, 6)
    bImg.fillRect(0, 0, 50, 6, 14)
    let bColor = isBerserk ? 2 : 4
bImg.fillRect(1, 1, Math.max(0, Math.floor(48 * bossHP / 100)), 4, bColor)
    bImg.drawRect(0, 0, 50, 6, 15)
    bossBar.setImage(bImg)
}
